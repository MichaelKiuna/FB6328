#include <iostream>

int main() {
    std::cout << "        O       O\n";    // Heads of the stick figures
    std::cout << "       /|\\     /|\\\n";  // Arms and bodies (holding hands)
    std::cout << "       / \\     / \\\n";  // Legs
    std::cout << "        |       |\n";   // Separation line to indicate hands are held
    std::cout << "    _________\n";      // Top of the car
    std::cout << "   /         \\\n";      // Upper part of the car
    std::cout << "  /___________\\\n";    // Bottom part of the car
    std::cout << "   o       o\n";        // Wheels of the car
    std::cout << "=================";   // Ground

    return 0;
}
